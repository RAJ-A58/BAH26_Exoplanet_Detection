def load_data(data_dir: str):
    """
    Load the saved numpy arrays.  Expects:
      X_global.npy  — shape (N, GLOBAL_BINS)
      X_local.npy   — shape (N, LOCAL_BINS)
      y_train.npy   — binary labels  (N,)
      y_class.npy   — per-class labels (N,)  [optional, generated by new dataset script]

    The holdout split uses the same random_state=42 as train_model.py so the
    validation set here is identical to what the model never saw during training.
    """
    print("  Loading dataset arrays...")
    X_global = np.load(os.path.join(data_dir, "X_global.npy"))
    X_local  = np.load(os.path.join(data_dir, "X_local.npy"))
    y_binary = np.load(os.path.join(data_dir, "y_train.npy"))

    # Per-class labels — optional (generate_dataset.py saves them as y_class.npy)
    class_path = os.path.join(data_dir, "y_class.npy")
    y_class = np.load(class_path) if os.path.exists(class_path) else None

    print(f"  Total samples: {len(y_binary)}")
    print(f"  Class distribution: {dict(zip(*np.unique(y_binary, return_counts=True)))}")

    # Reproduce the exact same 80/20 split used during training
    if y_class is not None:
        _, X_g_val, _, X_l_val, _, y_val, _, y_class_val = train_test_split(
            X_global, X_local, y_binary, y_class,
            test_size=0.2, random_state=42, stratify=y_binary
        )
    else:
        _, X_g_val, _, X_l_val, _, y_val = train_test_split(
            X_global, X_local, y_binary,
            test_size=0.2, random_state=42, stratify=y_binary
        )
        y_class_val = None

    print(f"  Validation set: {len(y_val)} samples")
    return X_g_val, X_l_val, y_val, y_class_val


def load_model(model_path: str):
    print(f"  Loading model from {model_path}...")
    return tf.keras.models.load_model(model_path)


